    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
          // get the article by id
          $article = Article::findOrFail($id);

        //   delete article
        if($article->delete()){
            return new ArticleResource($article);
        }
    }
